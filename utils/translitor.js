function trEnRu(text) {
return text
.replace(/\q/g,"é")
.replace(/w/g,"ö")
.replace(/\e/g,"ó")
.replace(/r/g,"ê")
.replace(/t/g,"å")
.replace(/\y/g,"í")
.replace(/\u/g,"ã")
.replace(/\i/g,"ø")
.replace(/\o/g,"ù")
.replace(/\p/g,"ç")
.replace(/\[/g,"õ")
.replace(/]/g,"ú")
.replace(/\a/g,"ô")
.replace(/s/g,"û")
.replace(/d/g,"â")
.replace(/f/g,"à")
.replace(/\g/g,"ï")
.replace(/\h/g,"ð")
.replace(/\j/g,"î")
.replace(/\k/g,"ë")
.replace(/\l/g,"ä")
.replace(/\;/g,"æ")
.replace(/\'/g,"ý")
.replace(/\z/g,"ÿ")
.replace(/\x/g,"÷")
.replace(/\c/g,"ñ")
.replace(/v/g,"ì")
.replace(/b/g,"è")
.replace(/n/g,"ò")
.replace(/\m/g,"ü")
.replace(/\,/g,"á")
.replace(/\./g,"þ")
.replace(/\//g,".")
.replace(/\?/g,",")
.replace(/\@/g,"\"")
.replace(/\#/g,"¹")
.replace(/\$/g,";")
.replace(/\^/g,":")
.replace(/\&/g,"?")

}
module.exports.trEnRu=trEnRu
